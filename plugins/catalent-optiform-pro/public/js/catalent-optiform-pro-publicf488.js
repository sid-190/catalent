    if (peff < 1) {
      An = 1;
    } else {
      An = peff;
    }
    SLAD = 500 * An * solubility;
    DS = dose / solubility;
    if (DS < 500) {
      DS_Var = 1;
    } else {
      DS_Var = 0;
    }
    if (peff < 1) {
      P_Var = 0;
    } else {
      P_Var = 1;
    }
    SDP = DS_Var * 10 + P_Var;
